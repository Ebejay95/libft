#include "./../libft.h"

void	ft_putbits_fd(int fd, int n)
{
	int	i;

	i = 7;
	while (i >= 0)
	{
		if (n & (1 << i))
			write(fd, "1", 1);
		else
			write(fd, "0", 1);
		i--;
	}
}
