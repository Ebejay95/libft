#include "./../libft.h"

void	ft_putbits(int n)
{
	int	i;

	i = 7;
	while (i >= 0)
	{
		if (n & (1 << i))
			write(1, "1", 1);
		else
			write(1, "0", 1);
		i--;
	}
}
